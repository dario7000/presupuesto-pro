const translations: Record<Lang, TranslationKeys> = { es, en, pt, fr, it, de }

export const getTranslations = (lang: Lang): TranslationKeys => translations[lang] || es

export const getStatusLabel = (status: string, lang: Lang): string => {
  const t = getTranslations(lang)
  const map: Record<string, string> = {
    draft: t.status_draft, sent: t.status_sent, accepted: t.status_accepted,
    rejected: t.status_rejected, in_progress: t.status_in_progress,
    completed: t.status_completed, paid: t.status_paid,
  }
  return map[status] || status
}
